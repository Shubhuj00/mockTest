class CheckNum{
    public static void main(String[] args) {
        int i=-10;
        if(i>0){
            System.out.println(i+ " is a positive number");       
        }else if(i<0){
            System.out.println(i+ "is a negative number");
        }else{
            System.out.println(i+ " is equal to zero");
        }
    }

}